const mongoose = require("mongoose");

const UserAccountSchema = new mongoose.Schema({
  username: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  ideas: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Idea",
    },
  ],
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("UserAccount", UserAccountSchema);
